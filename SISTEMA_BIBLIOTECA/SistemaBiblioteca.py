# Crie uma aplicação para gerenciar uma biblioteca. Implemente as seguintes classes:

# Livro: com atributos como título, autor, ISBN e disponibilidade (se está emprestado ou não). Inclua métodos para emprestar e devolver o livro.
# Usuario: com atributos como nome, ID e uma lista de livros emprestados. Inclua métodos para pegar e devolver livros.
# Biblioteca: que mantém um catálogo de livros. Implemente métodos para adicionar, remover e listar livros disponíveis.
# Adicione regras, como:

# Um usuário pode emprestar no máximo 3 livros.
# Um livro só pode ser emprestado se estiver disponível.